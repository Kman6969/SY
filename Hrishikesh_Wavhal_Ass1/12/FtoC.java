class Celsius{  
    public static void main (String args[]){
		float Fahrenheit, Celsius;  
           Fahrenheit = 64;  
           Celsius  = ((Fahrenheit-32)*5)/9;
		   
           System.out.println("Temperature in fahrenheit is: "+Fahrenheit);	
           System.out.println("Temperature in celsius is: "+Celsius);  
    }
}  